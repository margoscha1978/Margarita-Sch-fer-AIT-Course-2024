package interesse;

public class FaktorialChisla {
    // Факториал числа — это произведение всех натуральных чисел от 1 до заданного числа.
    // Например, факториал числа 5 равен 5 * 4 * 3 * 2 * 1 = 120.
    // В Java программу для нахождения факториала заданного числа можно написать с помощью цикла for или рекурсии.
    // Ниже приведен пример программы с использованием цикла for:

    public class FactorialCalculator {
        public static void main(String[] args) {
            int number = 5; // Заданное число, для которого мы хотим найти факториал
            int factorial = 1; // Изначально присваиваем факториалу значение 1

            // Используем цикл for для нахождения факториала
            for (int i = 1; i <= number; i++) {
                factorial *= i; // Умножаем текущее значение факториала на текущее значение i
            }

            // Выводим результат
            System.out.println("Факториал числа " + number + " равен " + factorial);
        }
    }
}
