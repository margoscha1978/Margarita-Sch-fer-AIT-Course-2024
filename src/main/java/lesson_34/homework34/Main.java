package lesson_34.homework34;
/*
Метод main:
   - Создается объект класса SimpleBooking.
   - Используются два массива: один для номеров комнат (включая некорректные),
     другой для имен гостей (включая некорректные).
   - В цикле for перебираются все комбинации номеров комнат и имен гостей, вызывая метод bookRoom.
   - Вся логика вызова метода находится в блоке try-catch, который обрабатывает возможные исключения
     и выводит понятные сообщения на экран.
 */
public class Main { // отработка класса SimpleBooking
    public static void main(String[] args) {
        SimpleBooking booking = new SimpleBooking();

        // Тестовые данные для вызова метода bookRoom
        int[] roomNumbers = {101, -1, 102, 0}; // Номера комнат (включая некорректные)
        String[] guestNames = {"Иван", null, "", "Петр"}; // Имена гостей (включая некорректные)

        for (int i = 0; i < roomNumbers.length; i++) {
            try {
                booking.bookRoom(roomNumbers[i], guestNames[i]); // Пытаемся забронировать комнату
            } catch (IllegalArgumentException e) {
                // Если произошло исключение, выводим сообщение об ошибке
                System.out.println("Некорректные данные бронирования: " + e.getMessage());
            }
        }
    }
} // klass ended

