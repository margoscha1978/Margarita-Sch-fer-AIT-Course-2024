package lesson_34.ClassWork34;


/**
 * This enum defines various categories that a gift can belong to.
 * Each category represents a type of gift.
 *
 * Этот перечислительный тип определяет различные категории, к которым может принадлежать подарок.
 * Каждая категория представляет тип подарка.
 */
public enum GiftCategory {
    ELECTRONICS, // Electronics category / Категория электроники
    TOYS,        // Toys category / Категория игрушек
    BOOKS,       // Books category / Категория книг
    CLOTHING,    // Clothing category / Категория одежды
    HOME_APPLIANCES // Home appliances category / Категория бытовой техники

} // klass ended
