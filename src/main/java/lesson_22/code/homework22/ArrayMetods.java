package homework22;
//Задача 1. Реализуйте в классе ArrayMethods следующие методы для массивов целых чисел:
//    сумма всех элементов массива с нечетными индексами;
//    поиск 5-го элемента в массиве. Разработайте тесты для этих методов.

public class ArrayMetods {
    // Метод для нахождения суммы всех элементов массива с нечетными индексами
    public int sumOddIndexElements(int[] array) {
        int sum = 0; // Переменная для хранения суммы

        // пробегаем по массиву
        for (int i = 0; i < array.length; i++) {
            // Проверяем, является ли индекс нечетным
            if (i % 2 != 0) {
                sum += array[i]; // Добавляем значение элемента к сумме
            }
        }
        return sum; // Возвращаем окончательную сумму
    }

    // Метод для поиска 5-го элемента массива (индекс 4)
    public int findFifthElement(int[] array) {
        if (array.length < 5) { // Проверяем, достаточно ли элементов в массиве
            throw new ArrayIndexOutOfBoundsException("Массив должен содержать минимум 5 элементов.");
        }
        return array[4]; // Возвращаем пятый элемент массива (индекс 4)
    }
}
