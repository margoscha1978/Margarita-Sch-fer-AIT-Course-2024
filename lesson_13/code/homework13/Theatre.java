package homework13;
// Упражнение 1: Подсчет свободных мест на спектакли: Напишите программу,
// которая запрашивает количество спектаклей и количество мест в зале для каждого спектакля,
// а затем количество проданных билетов на каждый спектакль.
// Программа должна вывести количество свободных мест на каждом спектакле и общее количество свободных мест.
//Пример ввода:
//Введите количество спектаклей: 3
//Введите количество мест в зале для спектакля 1: 200
//Введите количество проданных билетов на спектакль 1: 150
//Введите количество мест в зале для спектакля 2: 180
//Введите количество проданных билетов на спектакль 2: 130
//Введите количество мест в зале для спектакля 3: 220
//Введите количество проданных билетов на спектакль 3: 210
//Пример вывода:
//Свободных мест на спектакле 1: 50
//Свободных мест на спектакле 2: 50
//Свободных мест на спектакле 3: 10
//Общее количество свободных мест: 110

import java.util.Scanner;

public class Theatre {
    public static void main(String[] args) {

        Scanner scanner = new Scanner(System.in);

        // Запрашиваем количество спектаклей
        System.out.print("Введите количество спектаклей: ");
        int numberOfShows = scanner.nextInt();

        // Создаем массивы для хранения данных
        int[] totalSeats = new int[numberOfShows]; // Массив для общего количества мест
        int[] soldTickets = new int[numberOfShows]; // Массив для проданных билетов

        // Ввод данных о каждом спектакле
        for (int i = 0; i < numberOfShows; i++) {
            System.out.print("Введите количество мест в зале для спектакля " + (i + 1) + ": ");
            totalSeats[i] = scanner.nextInt(); // Запись общего количества мест

            System.out.print("Введите количество проданных билетов на спектакль " + (i + 1) + ": ");
            soldTickets[i] = scanner.nextInt(); // Запись количества проданных билетов
        }

        int totalAvailableSeats = 0; // Переменная для хранения общего количества свободных мест

        // Вывод количества свободных мест для каждого спектакля
        for (int i = 0; i < numberOfShows; i++) {
            int availableSeats = totalSeats[i] - soldTickets[i]; // Расчет свободных мест
            totalAvailableSeats += availableSeats; // Суммируем свободные места
            System.out.println("Свободных мест на спектакле " + (i + 1) + ": " + availableSeats);
        }

        // Вывод общего количества свободных мест
        System.out.println("Общее количество свободных мест: " + totalAvailableSeats);
       }

    } // klass ende