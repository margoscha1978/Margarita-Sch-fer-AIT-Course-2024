package homework13;
// Упражнение 2: Подсчет продаж билетов на театральные постановки: Напишите
// программу, которая запрашивает количество театральных постановок за месяц и
// количество проданных билетов для каждой постановки. Программа должна вывести
// общее количество проданных билетов и среднее количество билетов, проданных за
// каждую постановку.
// Пример ввода:
// Введите количество театральных постановок за месяц: 4
// Введите количество проданных билетов на постановку 1: 120
// Введите количество проданных билетов на постановку 2: 150
// Введите количество проданных билетов на постановку 3: 130
// Введите количество проданных билетов на постановку 4: 140
// Пример вывода:
// Общее количество проданных билетов: 540
// Среднее количество проданных билетов: 135.0

import java.util.Scanner;

public class TheatrTickets {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        // Шаг 1: Ввод количества театральных постановок
        System.out.print("Введите количество театральных постановок за месяц: ");
        int numPerformances = scanner.nextInt();

        // Инициализация переменной для подсчета общего количества билетов
        int totalTickets = 0;

        // Шаг 2: Ввод количества проданных билетов для каждой постановки
        for (int i = 1; i <= numPerformances; i++) {
            System.out.print("Введите количество проданных билетов на постановку " + i + ": ");
            int ticketsSold = scanner.nextInt();
            totalTickets += ticketsSold; // Суммируем количество билетов
        }

        // Шаг 3: Подсчет среднего количества билетов
        double averageTickets = (double) totalTickets / numPerformances;

        // Шаг 4: Вывод результатов
        System.out.println("Общее количество проданных билетов: " + totalTickets);
        System.out.printf("Среднее количество проданных билетов: %.1f\n", averageTickets);

        // Закрываем сканер
        scanner.close();
    }

}// klass ende
