package homework13;
// Упражнение 4:
// Оценки спектаклей зрителями: Описание: Напишите программу,
// которая запрашивает количество зрителей и оценки, которые они поставили
// спектаклю (по шкале от 1 до 10). Программа должна вывести количество каждой
// оценки и определить, сколько человек оценили спектакль на 9 или 10 баллов. Пример:
// Пример ввода:
// Введите количество зрителей: 5
// Введите оценку зрителя 1: 8
// Введите оценку зрителя 2: 9
// Введите оценку зрителя 3: 7
// Введите оценку зрителя 4: 10
// Введите оценку зрителя 5: 6
// Пример вывода:
//  Оценки:
// 1 баллов: 0
// 2 баллов: 0
// 3 баллов: 0
// 4 баллов: 0
// 5 баллов: 0
// 6 баллов: 1
// 7 баллов: 1
// 8 баллов: 1
// 9 баллов: 1
// 10 баллов: 1
// Число зрителей, оценивших спектакль на 9 или 10 баллов: 2

import java.util.Scanner;

public class HingRatings {
    public static void main(String[] args) {
        // 1. Запросить количество зрителей.
        // 2. Запросить оценки от каждого зрителя.
        // 3. Подсчитать количество каждой оценки от 1 до 10.
        // 4. Определить, сколько зрителей поставили оценку 9 или 10.
        // 5. Вывести результаты.

        Scanner scanner = new Scanner(System.in);// для считывания пользовательского ввода.

        // Шаг 1: Запрашиваем количество зрителей
        System.out.print("Введите количество зрителей: ");
        int numberOfViewers = scanner.nextInt();

        // Массив для подсчета оценок
        int[] ratingsCount = new int[11]; // индексы 0-10, индекс 0 игнорируем

        // Шаг 2: Запрашиваем оценки у зрителей
        for (int i = 1; i <= numberOfViewers; i++) {
            System.out.print("Введите оценку зрителя " + i + ": ");
            int rating = scanner.nextInt();
            // Проверяем, что оценка в пределах 1-10
            if (rating >= 1 && rating <= 10) {
                ratingsCount[rating]++;
            } else {
                System.out.println("Ошибка: оценка должна быть от 1 до 10.");
                i--; // Снижаем счётчик, если была ошибка
            }
        }

        // Шаг 3: Выводим результаты
        System.out.println("\nОценки:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i + " баллов: " + ratingsCount[i]);
        }

        // Шаг 4: Подсчитываем количество оценок 9 и 10
        int countOfHighRatings = ratingsCount[9] + ratingsCount[10];
        System.out.println("Число зрителей, оценивших спектакль на 9 или 10 баллов: " + countOfHighRatings);
    }

} // klass ende
